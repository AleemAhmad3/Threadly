const mongoose = require("mongoose");

const userSchema = new mongoose.Schema(
  {
    name: String,
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      index: true,
    },
    password: { type: String, required: true },
    avatar: { type: String },
  },
  { timestamps: true },
);

module.exports = mongoose.model("User", userSchema);
